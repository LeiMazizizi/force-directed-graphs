-- create db and user
CREATE DATABASE FORCE_DIRECTED_GRAPHS;
CREATE USER 'mysql'@'localhost' IDENTIFIED BY 'mysql_password';
GRANT ALL ON FORCE_DRIECTED_GRAPHS.* TO 'mysql'@'localhost';
FLUSH PRIVILEGES;

-- create tables for nodes and edges
USE FORCE_DIRECTED_GRAPHS;
CREATE TABLE NODE(
	ID INT NOT NULL AUTO_INCREMENT UNIQUE PRIMARY KEY,
	COLOR INT NOT NULL,
	DEGREE INT NOT NULL
);

CREATE TABLE EDGE(
	SOURCE INT FOREIGN KEY REFRENCES NODE(SOURCE),
	TARGET INT FOREIGN KEY REFRENCES NODE(TARGET),
	WEIGHT INT NOT NULL
);

CREATE TABLE CLIQUE(
	ID INT NOT NULL AUTO_INCREMENT UNIQUE PRIMARY KEY,
	AMMO INT NOT NULL,
	MEMBERS XML
);

